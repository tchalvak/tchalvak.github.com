<a name="overview" class="brownHeading">What is Ninja Wars ?</a>

<p style="text-align: justify;">
Ninja Wars is a web based role playing game where the player assumes the role of a Ninja. In Ninja Wars you will battle against other ninjas and ninja clans for money and rank.
</p>

<a href="about.php">Return to Contents</a>

<hr />

<a name="gameplay" class="brownHeading">How does the game play ?</a>

<p style="text-align: justify;">
The game plays by you (a player) creating an account and then logging in. Once logged in you will be able to perform a variety of tasks; attacking, training, and more.
</p>

<a href="about.php">Return to Contents</a>

<hr />

<a name="turns" class="brownHeading">Turns</a>

<p style="text-align: justify;">
Turns are the life force of this game, you need turns to perform certain actions. Your turns will replenish at a rate of 1 per hour.
</p>

<a href="about.php">Return to Contents</a>

<hr />

<a name="work" class="brownHeading">Work</a>

<p style="text-align: justify;">
Working allows your Ninja to have a day job. This allows you to trade Turns for Gold. An easy, direct way to make effortless profit.
</p>

<a href="about.php">Return to Contents</a>

