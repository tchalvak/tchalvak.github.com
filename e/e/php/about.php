<p style="text-align: justify;">
To use an item, click on Inventory from the menu. You will be given a selection screen similar to making a phrase. The first field is a drop down list of all items you currently posses. The final list is a drop box of all available users.
<br /><br />
Example: Use <span style="text-decoration: underline;">Fire Scroll</span> on <span style="text-decoration: underline;">ninjalord</span>. This will cause the 'Fire Scroll' item to be used on 'ninjalord', which will reduce ninjalord's hp.
